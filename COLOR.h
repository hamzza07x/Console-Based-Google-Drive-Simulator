#ifndef COLOR_H
#define COLOR_H
//ANSI codes for text color formating in terminal
#define RED "\033[31m"//RED color for error messeges or alerts
#define GREEN "\033[32m"//GREEN color for success messeges or prompts
#define BLUE "\033[34m"//BLUE color for informational messeges
#define PINK "\033[35m"//PINK color for additional highlights
#define YELLOW "\033[33m"//YELLOW color for key details or warnings
#define RESET "\033[0m"//RESET color to default after applying any color
#endif